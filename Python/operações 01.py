# Operadores aritméticos

v1 = int(input("Informe o primeiro valor: "))
v2 = int(input("Informe o segundo valor: "))

print("\nAs operações aritméticas com os dois valores informados são o seguinte:\n")

print("Soma: {}" .format(v1 + v2))
print("Subtração: {}" .format(v1 - v2));
print("Multiplicação: {}" .format(v1 * v2))
print("Divisão: {}" .format(v1 / v2))
print("Resto da divisão: {}" .format(v1 % v2))
print("Potência: {}" .format(v1 ** v2))
print("Divisão inteira: {}" .format(v1 // v2))

